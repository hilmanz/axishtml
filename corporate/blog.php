                        <h2>Membangun (Kembali) Rasa Kepercayaan di Lingkungan Kerja</h2>
                        <p>Saya baru-baru berpartisipasi dalam sebuah seminar dengan tema "Mengembalikan Kepercayaan Bisnis" . Di ujung seminar, 
                        terjadi diskusi antar para peserta tentang hal-hal yang kita semua ingin hindari, dan juga cara memahami cerita  
                        (bagaimana hal tersebut terjadi) untuk mencoba  menghindarinya jika hal tersebut kembali terjadi.</p>
                        <p>Hal yang sama berlaku untuk "good story". Saya kutip sebagai contoh: fakta bahwa edisi-edisi majalah Fortune yang mengangkat cerita
                        tentang Perusahaan Terbaik untuk Bekerja dan cerita tentang Orang - Orang Paling Dikagumi di dalamnya secara konsisten merupakan edisi 
                        dengan penjualan terbaik. Orang ingin membaca cerita baik dalam dua isu tersebut:  tentang bagaimana pemimpin menciptakan ruang kerja 
                        yang nyaman, apa yang karyawan pikirkan tentang bos mereka, dan perusahaan mana yang paling dikagumi di seluruh dunia.
                         Hal-hal seperti ini selalu menarik perhatian.</p>
                        <p>Jika kita ingin orang mempercayai sebuah usaha - atau lembaga apapun - kita perlu membantu mereka terhubung dengan orang - 
                        orang yang merupakan bagian dari institusi tersebut. Dalam sebuah lembaga, pemimpin perlu untuk membantu orang-orang di dalamnya 
                        terhubung satu sama lain, untuk menciptakan ikatan kepercayaan yang akan memperkuat organisasi dan, sebagai sisi manfaat yang indah, 
                        membuat cerita positif yang dapat diceritakan untuk menjaga proses membangun kepercayaan di lingkungan kerja.</p>
                        <p>Kepercayaan dalam perusahaan akan semakin kuat ketika kita terlibat dalam percakapan terbuka dan jujur tentang pentingnya 
                        kepercayaan dan selalu berbagi cerita pada pemimpin, yang kemudian akan membangun kepercayaan yang besar sehingga mereka 
                        dapat menjadi contoh bagi orang lain.</p>

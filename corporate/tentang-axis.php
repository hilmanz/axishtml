<h2>Kami adalah operator GSM dengan pertumbuhan paling pesat di Indonesia</h2>
<p>Memasuki tahun keempat operasinya AXIS telah menyediakan dana lebih dari USD 1,2 milyar untuk melakukan ekspansi jaringan dan infrastrukturnya, memperluas wilayah layanan mobile broadband, serta mengimplementasikan teknologi HSPA+ selama tiga tahun kedepan. Saat ini AXIS telah menjangkau lebih dari 80% populasi dan tersedia di lebih dari 400 kota utama di Indonesia.</p>
<p>AXIS menyampaikan terima kasih kepada seluruh pelanggan atas keyakinan dan kepercayaan yang diberikan.</p>
<p>Kami juga berpartisipasi aktif dalam kegiatan yang berhubungan dengan pendidikan dan pelestarian lingkungan.</p>
<h2>AXIS didukung oleh dua operator terkemuka di Asia sebagai pemegang saham: </h2>
<ol>
  <li>Saudi Telecom Company (STC). Perusahaan telekomunikasi global yang didirikan pada tahun 1998 ini adalah penyedia layanan telekomunikasi nasional terdepan di Kerajaan Arab Saudi dan hingga kini masih menjadi satu-satunya penyedia layanan telepon tetap, data, dan internet di sana. STC beroperasi melalui empat anak usahanya (Alhatif, Aljawal, Saudi Data, and Saudinet) serta memiliki beberapa perusahaan yang tersebar lebih dari 10 negara, termasuk Kuwait, Bahrain, India, Indonesia, Malaysia, Turki, Yordania, Libanon dan Afrika Selatan. <br />
    <br />
  Komitmen jangka panjang STC untuk mengembangkan industri telekomunikasi di Indonesia baru-baru ini dibuktikan dengan peningkatan kepemilikannya di AXIS hingga 80%.<br />
  <br />
  AXIS adalah satu-satunya operator seluler yang memiliki hubungan dengan STC, operator terbesar di Arab Saudi, yang memberi kenyamanan maksimal konsumennya pada saat menjalankan ibadah Haji maupun Umrah.<br />
  <br />
  </li>
  <li>Maxis Communications Berhad (Maxis). Penyedia layanan telekomunikasi terbesar di Malaysia yang memiliki investasi di industri telekomunikasi di India dan Indonesia ini menyediakan layanan telepon seluler dan tetap serta layanan gateway internasional.</li>
</ol>
<p>Kedua investor utama kami bertekad memberikan kontribusi penuh bagi pengembangan industri telekomunikasi di Indonesia.</p>
<h2>AXIS Kini dan Masa Depan</h2>
<p>Bertekad untuk memperkuat sumber daya manusia kami dan menciptakan lebih banyak kesempatan kerja untuk anak bangsa baik langsung maupun tidak langsung.<br />
  Rencana konkret dan pendanaan disiapkan untuk membangun lebih dari 6.000 Base Transceiver Station (BTS) dalam tiga tahun kedepan.<br />
  AXIS akan mempersatukan Indonesia dengan layanan mobile broadband dengan menggunakan teknologi yang paling mutakhir.</p>


						<h2>Sekilas tentang AXIS</h2>
						<p>AXIS adalah operator GSM nasional dengan pertumbuhan tercepat di Indonesia. Perusahaan ini menyediakan layanan 2G, 3G, dan BlackBerry secara nasional dan menjangkau seluruh dunia melalui lebih dari 400 mitra roaming internasional di lebih dari 165 negara.</p>
						<p>AXIS memiliki misi untuk mewujudkan layanan telepon, SMS, data dan fitur-fitur layanan lain tersedia dan terjangkau bagi semua masyarakat Indonesia. Pertama kali meluncurkan layanannya tahun 2008, AXIS memperkenalkan pendekatan yang berbeda pada penawarannya melalui produk dan layanan yang sederhana, mudah dipahami dan tarif yang transparan. AXIS juga menantang norma pasar dengan menghadirkan penawaran yang jujur tanpa syarat dan ketentuan tersembunyi.</p>
						<p>Saat ini AXIS telah menjangkau lebih dari 70% populasi di Indonesia, yang meliputi Jawa, Bali, Lombok, Sumatra Barat, Sumatra Utara dan Riau. Layanan HSPA+ AXIS  kini tersedia di kota-kota besar seperti Jabodetabek, Serang, Cilegon, Bandung, Semarang, Jogjakarta, Solo, Denpasar, Mataram, Kediri Madiun, Jember, Malang, Surabaya, Purwokerto, Tegal, Sukabumi, Tasikmalaya, Cirebon dan akan dilanjutkan ke kota-kota lainnya.</p>
					
